OS GERENTES QUEREM SABER QUAIS PRODUTOS NÃO ESTÃO TRAZENDO EM MÉDIA NO MIN 1M EM TOTAL DE VENDAS

SELECT productid,AVG(linetotal)
FROM Sales.SalesOrderDetail
GROUP BY ProductID
HAVING AVG(linetotal) < 1000000