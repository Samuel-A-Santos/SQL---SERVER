DESAFIO 1
ESTAMOS QUERENDO IDENTIFICAR AS PROVINCIAS COM MAIOR NUMERO DE CADASTROS NO NOSSO SISTEMA, ENTÃO É PRECISO ENCONTRAR QUAIS PROVINCIAS ESTÃO REGISTRADAS NO BANCO DE DADOS MAIS QUE 1000X.

SELECT stateprovinceid,COUNT(stateprovinceid)
FROM Person.Address
GROUP BY StateProvinceID
HAVING COUNT(stateprovinceid) > 1000


